# ----------------------------------------
# FastAPI App
# ----------------------------------------

app = FastAPI()

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# ----------------------------------------
# Request Model
# ----------------------------------------

class StudyPlanRequest(BaseModel):
    module: str
    score: int

# ----------------------------------------
# API Endpoint
# ----------------------------------------

@app.post("/generate_plan")
async def generate_plan_endpoint(req: StudyPlanRequest):
    try:
        # build_prompt() and llm should already exist
        prompt = build_prompt(req.module, req.score)

        response = llm.invoke(prompt)

        return {
            "success": True,
            "plan": response.content
        }

    except Exception as e:
        return {
            "success": False,
            "error": str(e)
        }

