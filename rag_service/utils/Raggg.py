def retrieve_context(question, module=None, k=5):

    if module:

        docs = vectordb.similarity_search(

            question,

            k=k,

            filter={"module":module}

        )

    else:

        docs = vectordb.similarity_search(

            question,

            k=k

        )

    context = "\n\n".join([doc.page_content for doc in docs])

    return context

